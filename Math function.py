# To use maths operation use math package

import math

print(dir(math))

print(math.factorial(5))
print(math.sqrt(4)) #square root
print(math.ceil(2.86)) #pick largest whole number
print(math.floor(2.86)) #pick lowest whole no.
print(math.fabs(-9)) #gives absolute value
print(math.pow(2, 4)) #power base,power
print(math.log(2,8)) # num,base
print(math.log2(2))
print(math.pi) #pi value



